export type Translations = typeof translations.es;
export type Language = 'es' | 'en';

interface LanguageContextType {
    language: Language;
    setLanguage: (lang: Language) => void;
    t: (key: keyof Translations, params?: Record<string, any>) => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LanguageProvider = ({ children }: { children: ReactNode }) => {
    const { profile, updateProfile } = useProfile();
    const [language, setLanguageState] = useState<Language>(() => {
        // Initial sync from localStorage or browser
        const cachedLang = localStorage.getItem('naos_language') as Language;
        if (cachedLang && (cachedLang === 'es' || cachedLang === 'en')) return cachedLang;
        return navigator.language.startsWith('es') ? 'es' : 'en';
    });

    useEffect(() => {
        // Sync language state with profile if it changes remotely
        if (profile && (profile as any).language && (profile as any).language !== language) {
            const lang = (profile as any).language as Language;
            if (lang === 'es' || lang === 'en') {
                setLanguageState(lang);
                localStorage.setItem('naos_language', lang);
            }
        }
    }, [profile, language]);

    const setLanguage = async (lang: Language) => {
        setLanguageState(lang);
        localStorage.setItem('naos_language', lang);
        
        try {
            if (profile) {
                await updateProfile({ language: lang } as any);
            }
        } catch (e) {
            console.warn("LanguageContext: Failed to update profile language field.", e);
        } finally {
            // Force full reload to ensure all data libraries/components refresh with new language
            window.location.reload();
        }
    };

    const t = (key: keyof Translations, params?: Record<string, any>): string => {
        const trans = translations[language] as any;
        let text = trans[key] || key;
        
        if (params) {
            Object.entries(params).forEach(([k, v]) => {
                text = text.replace(`{{${k}}}`, v.toString());
            });
        }
        
        return text;
    };

    return React.createElement(LanguageContext.Provider, { value: { language, setLanguage, t } }, children);
};

export const useTranslation = () => {
    const context = useContext(LanguageContext);
    if (!context) {
        throw new Error('useTranslation must be used within a LanguageProvider');
    }
    return context;
};
